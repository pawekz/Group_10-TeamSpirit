import java.util.Scanner;

 class CashInputExample {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        System.out.println("Welcome to the Cash Input Program!");

        // Get the item price from the user
        System.out.print("Enter the item price: $");
        double itemPrice = scanner.nextDouble();

        // Get the amount paid by the user
        System.out.print("Enter the amount paid: $");
        double amountPaid = scanner.nextDouble();

        // Calculate the change
        double change = amountPaid - itemPrice;

        // Display the change
        if (change >= 0) {
            System.out.println("Change: $" + change);
            calculateChangeDenominations(change);
        } else {
            System.out.println("Insufficient payment. Please provide enough funds.");
        }

        scanner.close();
    }

    private static void calculateChangeDenominations(double change) {
        int[] denominations = {100, 50, 20, 10, 5, 1, 25, 10, 5, 1}; // Assuming dollars and cents
        String[] names = {"$100", "$50", "$20", "$10", "$5", "$1", "quarters", "dimes", "nickels", "pennies"};

        System.out.println("Change breakdown:");

        for (int i = 0; i < denominations.length; i++) {
            int count = (int) (change / denominations[i]);
            if (count > 0) {
                System.out.println(names[i] + ": " + count);
                change -= count * denominations[i];
            }
        }
    }
}
