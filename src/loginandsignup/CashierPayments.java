    private String generateRandomVoucherCode() {
        // Define the characters allowed in the voucher code
        String allowedCharacters = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";

        // Define the length of the voucher code
        int voucherCodeLength = 10;

        // Create a random object
        Random random = new Random();

        // Generate the voucher code
        StringBuilder voucherCodeBuilder = new StringBuilder();
        for (int i = 0; i < voucherCodeLength; i++) {
            int randomIndex = random.nextInt(allowedCharacters.length());
            char randomChar = allowedCharacters.charAt(randomIndex);
            voucherCodeBuilder.append(randomChar);
        }

        return voucherCodeBuilder.toString();
    }

